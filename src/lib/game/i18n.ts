// DUSKFALL i18n module — 6-language UI dictionary + helpers.
//
// Russian ("ru") is the PRIMARY language and the source of truth. All other
// languages are partial translations; any missing key falls back to Russian,
// and if the key is also missing in Russian the literal key string is
// returned. This means the game continues to work exactly as before for
// Russian users and degrades gracefully for partial translations.
//
// The `t(lang, key, params?)` function performs `{param}` placeholder
// substitution on the resolved string.

/** Supported language codes. Russian is the primary/default. */
export type Lang = "ru" | "en" | "es" | "de" | "fr" | "zh";

/** Metadata for the language picker (flag + native label). */
