/**
 * Translate `key` into `lang`. Falls back to Russian if the key is missing
 * from the target language, then to the literal key if also missing in
 * Russian. `{param}` placeholders in the resolved string are substituted
 * from the optional `params` map.
 */
export function t(lang: Lang, key: string, params?: Record<string, string | number>): string {
  const entry = DICT[lang];
  let value: string | undefined;
  if (entry) value = entry[key];
  if (value === undefined) {
    // Fallback to Russian (master dictionary).
    value = DICT.ru[key];
  }
  if (value === undefined) {
    // Final fallback: the literal key.
    return key;
  }
  if (params) {
    for (const [k, v] of Object.entries(params)) {
      value = value.split(`{${k}}`).join(String(v));
    }
  }
  return value;
}
