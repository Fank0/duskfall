// DUSKFALL i18n module — 6-language UI dictionary + helpers.
//
// Russian ("ru") is the PRIMARY language and the source of truth. All other
// languages are partial translations; any missing key falls back to Russian,
// and if the key is also missing in Russian the literal key string is
// returned. This means the game continues to work exactly as before for
// Russian users and degrades gracefully for partial translations.
//
// The `t(lang, key, params?)` function performs `{param}` placeholder
// substitution on the resolved string.

/** Supported language codes. Russian is the primary/default. */
export type Lang = "ru" | "en" | "es" | "de" | "fr" | "zh";

/** Metadata for the language picker (flag + native label). */
export const LANGS: { code: Lang; label: string; flag: string }[] = [
  { code: "ru", label: "Русский", flag: "🇷🇺" },
  { code: "en", label: "English", flag: "🇬🇧" },
  { code: "es", label: "Español", flag: "🇪🇸" },
  { code: "de", label: "Deutsch", flag: "🇩🇪" },
  { code: "fr", label: "Français", flag: "🇫🇷" },
  { code: "zh", label: "中文", flag: "🇨🇳" },
];

/** Default language — Russian. */
export function defaultLang(): Lang {
  return "ru";
}

/**
 * Returns the language name in its own native form, suitable for use inside
 * LLM system prompts that instruct the model which language to narrate in.
 * e.g. "русский" for ru, "English" for en, "Español" for es.
 */
export function llmLangName(lang: Lang): string {
  switch (lang) {
    case "ru":
      return "русский";
    case "en":
      return "English";
    case "es":
      return "Español";
    case "de":
      return "Deutsch";
    case "fr":
      return "Français";
    case "zh":
      return "中文";
    default:
      return "русский";
  }
}

